# Define a function to print the Sudoku board in a readable format
def print_board(board):
    """Prints the Sudoku board with grid lines."""
    for i in range(9):
        if i % 3 == 0 and i != 0:
            print("-" * 21)
        for j in range(9):
            if j % 3 == 0 and j != 0:
                print("| ", end="")
            if j == 8:
                print(board[i][j])
            else:
                print(str(board[i][j]) + " ", end="")

# Define a function to find an empty cell (represented by 0)
def find_empty(board):
    """Finds the next empty cell (0) in the board."""
    for i in range(len(board)):
        for j in range(len(board[0])):
            if board[i][j] == 0:
                return (i, j)  # row, col
    return None

# Define a function to check if a number is valid in a given position
def is_valid(board, num, pos):
    """Checks if a number is valid in the given position (pos)."""
    # Check row
    for i in range(len(board[0])):
        if board[pos[0]][i] == num and pos[1] != i:
            return False

    # Check column
    for i in range(len(board)):
        if board[i][pos[1]] == num and pos[0] != i:
            return False

    # Check 3x3 box
    box_x = pos[1] // 3
    box_y = pos[0] // 3

    for i in range(box_y * 3, box_y * 3 + 3):
        for j in range(box_x * 3, box_x * 3 + 3):
            if board[i][j] == num and (i, j) != pos:
                return False

    return True

# Define the main solving function using backtracking
def solve_sudoku(board):
    """Solves the Sudoku puzzle using backtracking."""
    find = find_empty(board)
    if not find:
        return True  # Puzzle solved
    else:
        row, col = find

    for i in range(1, 10):
        if is_valid(board, i, (row, col)):
            board[row][col] = i

            if solve_sudoku(board):
                return True  # If a valid solution is found, return True

            board[row][col] = 0  # Reset the cell if the current path fails

    return False  # Trigger backtracking

